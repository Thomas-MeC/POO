// classe
public class data 
{
	private int dia;
	private int mês;
	private int ano;

	
// métodos para retorno
public int getDia()
{
	return dia;
}

public int getMês()
{
	return mês;
}

public int getAno()
{
	return ano;
}

// construtor
 public data (int diaEscolhido, int mêsEscolhido, int anoEscolhido) 
 {
	 dia = diaEscolhido;
	 mês = mêsEscolhido;
	 ano = anoEscolhido;
 }
 

	 
 }